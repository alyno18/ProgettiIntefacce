package StreamCaratteri.GestioneFileTestuali;

public class Player {
    
    private String name;
    private String surname;
    private int score;

    public Player(String name, String surname, int score) {
        this.name = name;
        this.surname = surname;
        this.score = score;
    }

    public String getName() {
        return this.name;
    }

    public String getSurname() {
        return this.surname;
    }

    public int getScore() {
        return this.score;
    }
}
